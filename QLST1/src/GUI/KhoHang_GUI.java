    private void hienthi() {
        String[] tieude = {"Mã loại kho hàng", "Tên kho hàng", "Địa chỉ kho hàng", "Ghi chú kho hàng"};
        ArrayList<KhoHang> dsKoH = KhoHangDAO.LayDanhSachKhoHang();
        DefaultTableModel dtm = (DefaultTableModel)tb_DanhsachKhoHang.getModel();
        dtm.setColumnIdentifiers(tieude);
        theater();
        dtm.setRowCount(0);
        for(KhoHang koh: dsKoH){
            Vector <Object>v=new Vector<Object>();
            v.add(koh.getMaKho());
            v.add(koh.getTenKho());
            v.add(koh.getDiaChiKho());
            v.add(koh.getGhiChuKho());
            dtm.addRow(v);
        }
        tb_DanhsachKhoHang.setModel(dtm);
    }

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String maKoH = txt_ma.getText();
            String tenKoH = txt_ten.getText();
            String dcKoH = txt_diachi.getText();
            String gcKoH = txt_ghichu.getText();
        if(txt_ma.getText().isEmpty()||txt_ten.getText().isEmpty()||txt_diachi.getText().isEmpty()||txt_ghichu.getText().isEmpty()){
            txt_ma.requestFocus();
            JOptionPane.showMessageDialog(this,"Không Được Để Trống Thông Tin","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
            return;
        }else if(KhoHangDAO.CheckMaKho(maKoH)==true){
            JOptionPane.showMessageDialog(this,"Trùng Mã Kho rồi nha","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            KhoHang koh = new KhoHang();
            koh.setMaKho(maKoH);
            koh.setTenKho(tenKoH);
            koh.setDiaChiKho(dcKoH);
            koh.setGhiChuKho(gcKoH);
            KhoHangDAO.themKhoHang(koh);
            hienthi();
            JOptionPane.showMessageDialog(this,"Thêm Loại Sản Phẩm Thành Công","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        try{
            KhoHang koh = new KhoHang();
            String maKoH = txt_ma.getText();
            int row = tb_DanhsachKhoHang.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Chọn Dòng Cần Xóa ");
                return;
            } else {
            int m = JOptionPane.showConfirmDialog(this, "Bạn có thật sự muốn xóa không ?", "Thông báo", JOptionPane.YES_NO_OPTION);
            if(m == JOptionPane.NO_OPTION){
                return;
            }else{
                boolean n = KhoHangDAO.deleteKhoHang(maKoH);
                if (n == true) {
                    JOptionPane.showMessageDialog(this, "Xóa Kho Hàng\t " + maKoH + " Thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa Kho Hàng\t " + maKoH + " Thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Xung đột khóa ngoại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } finally {
            hienthi();
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        try {
            KhoHang koh = new KhoHang();
            koh.setTenKho(txt_ten.getText());
            koh.setDiaChiKho(txt_diachi.getText());
            koh.setGhiChuKho(txt_ghichu.getText());
            koh.setMaKho(txt_ma.getText());
            boolean n = KhoHangDAO.updateKhoHang(koh);
            int row = tb_DanhsachKhoHang.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Chọn Dòng Cần Sửa ");
                return;
            } else {
                if (n == true) {
                    JOptionPane.showMessageDialog(this, "Sửa Kho Hàng "  + txt_ma.getText() + " Thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa Kho Hàng "  + txt_ma.getText() + " Thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sửa Kho Hàng thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } finally {
            hienthi();
        }
    }//GEN-LAST:event_btn_suaActionPerformed


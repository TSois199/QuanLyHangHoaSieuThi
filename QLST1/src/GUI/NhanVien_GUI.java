    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        try {
            NhanVien nv = new NhanVien();
            nv.setTenNhanVien(txt_tennv.getText());
            nv.setEmailNhanVien(txt_email.getText());
            nv.setGioiTinh((String) cbb_gioitinh.getSelectedItem());
            nv.setSDT((String)(txt_sdt.getText()));
            nv.setDiaChi(txt_diachi.getText());
            nv.setMaNhanVien(txt_manv.getText());
            boolean n = NhanVienDAO.capNhatNhanVien(nv);
            int row = tb_DanhSachNhanVien.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Chọn Dòng Cần Sửa ");
                return;
            } else {
                if (n == true) {
                    JOptionPane.showMessageDialog(this, "Sửa Nhân Viên\t "  + txt_manv.getText() + " Thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa Nhân Viên\t"  + txt_manv.getText() + " Thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sửa Nhân Viên thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } finally {
            hienthi();
        }
    }//GEN-LAST:event_btn_suaActionPerformed



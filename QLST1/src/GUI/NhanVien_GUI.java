    private void hienthi() {
        theater();
        String[] tieude = {"Mã nhân viên", "Họ tên nhân viên", "Email", "Giới tính", "Số điện thoại", "Địa chỉ"};
        ArrayList<NhanVien> dsNV = NhanVienDAO.LayDanhSachNhanVien();
        DefaultTableModel dtm = (DefaultTableModel)tb_DanhSachNhanVien.getModel();
        dtm.setColumnIdentifiers(tieude);
        dtm.setRowCount(0);
        for(NhanVien nv: dsNV){
            Vector <Object>v=new Vector<Object>();
            v.add(nv.getMaNhanVien());
            v.add(nv.getTenNhanVien());
            v.add(nv.getEmailNhanVien());
             v.add(nv.getGioiTinh());
            v.add(nv.getSDT());
            v.add(nv.getDiaChi());
            dtm.addRow(v);
        }
        tb_DanhSachNhanVien.setModel(dtm);
    }


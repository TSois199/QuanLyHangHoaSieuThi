    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String maNV = txt_manv.getText();
            String tenNV = txt_tennv.getText();
            String emailNV = txt_email.getText();
            String gioiTinhNV = (String) cbb_gioitinh.getSelectedItem();
            String sdtNV = txt_sdt.getText();
            String diaChiNV = txt_diachi.getText();
        if(txt_manv.getText().isEmpty()||txt_tennv.getText().isEmpty()||txt_email.getText().isEmpty()||txt_sdt.getText().isEmpty()||txt_diachi.getText().isEmpty()){
            txt_manv.requestFocus();
            JOptionPane.showMessageDialog(this,"Không Được Để Trống Thông Tin","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
            return;
        }else if(NhanVienDAO.CheckMaNhanVien(maNV)==true){
            JOptionPane.showMessageDialog(this,"Trùng Mã Nhân Viên rồi nha","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            NhanVien nv = new NhanVien();
            nv.setMaNhanVien(maNV);
        nv.setTenNhanVien(tenNV);
        nv.setEmailNhanVien(emailNV);
        //nv.setNgaysinh(DateNV);
        nv.setGioiTinh(gioiTinhNV);
        nv.setSDT(sdtNV);
        //nv.setAnhNV(duongdananh);
        nv.setDiaChi(diaChiNV);
            NhanVienDAO.themNhanVien(nv);
            hienthi();
            JOptionPane.showMessageDialog(this,"Thêm Nhân Viên Thành Công","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int row =tb_DanhSachNhanVien.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this,"Chọn Dòng Cần Xóa ");
            return;
        }else{
            int n = JOptionPane.showConfirmDialog(this,"Thông Báo","Chắc Xóa Chưa",JOptionPane.YES_NO_OPTION);
            if(n==JOptionPane.NO_OPTION){
                return;
            }else{
                String ma = (String)tb_DanhSachNhanVien.getValueAt(row, 0);
                NhanVienDAO.xoaNhanVien(ma);
                hienthi();
                JOptionPane.showMessageDialog(this,"Xoá Thành Công","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        hienthi();
    }//GEN-LAST:event_btn_xoaActionPerformed


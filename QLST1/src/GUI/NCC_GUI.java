    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String maNCC = txt_mancc.getText();
        String tenNCC = txt_tenncc.getText();
        String diachiNCC = txt_diachincc.getText();
        if(txt_mancc.getText().isEmpty()||txt_tenncc.getText().isEmpty()||txt_diachincc.getText().isEmpty()){
            txt_mancc.requestFocus();
            JOptionPane.showMessageDialog(this,"Không Được Để Trống Thông Tin","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
           return;
        }else if(NhaCungCapDAO.CheckMaNCC(maNCC)==true){
            JOptionPane.showMessageDialog(this,"Trùng Mã Nhà Cung Cấp rồi nha","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
        NhaCungCap ncc = new NhaCungCap();
        ncc.setMaNhaCungCap(maNCC);
        ncc.setTenNhaCungCap(tenNCC);
        ncc.setDiaChiNhaCungCap(diachiNCC);
        NhaCungCapDAO.themNhaCungCap(ncc);
        hienthi();
        JOptionPane.showMessageDialog(this,"Thêm Nhà Cung Cấp Thành Công","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_tktheotenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tktheotenActionPerformed
        // TODO add your handling code here:
        int count = 0;
        String tenNCC= txt_tenncc.getText();
        dtm.setRowCount(0);
        ArrayList<NhaCungCap> dsNCC = NhaCungCapDAO.findNhaCungCapTheoTenNCC(tenNCC);
        for (NhaCungCap ncc : dsNCC) {
            Vector<Object> v = new Vector<Object>();
            v.add(ncc.getMaNhaCungCap());
            v.add(ncc.getTenNhaCungCap());
            v.add(ncc.getDiaChiNhaCungCap());
            dtm.addRow(v);
            count++;
        }
        tb_DanhSachNCC.setModel(dtm);
        JOptionPane.showMessageDialog(this, "Tìm kiếm thấy " + count + " Nhà Cung Cấp có tên " + tenNCC, "Thong bao", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btn_tktheotenActionPerformed


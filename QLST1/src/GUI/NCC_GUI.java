    private void hienthi() {
        theater();
        String[] tieude = {"Mã nhà cung cấp", "Tên nhà cung cấp", "Địa chỉ nhà cung cấp"};
        ArrayList<NhaCungCap> dsNCC = NhaCungCapDAO.LayDanhSachNhaCungCap();
        DefaultTableModel dtm = (DefaultTableModel)tb_DanhSachNCC.getModel();
        dtm.setColumnIdentifiers(tieude);
        dtm.setRowCount(0);
        for(NhaCungCap ncc: dsNCC){
            Vector <Object>v=new Vector<Object>();
            v.add(ncc.getMaNhaCungCap());
            v.add(ncc.getTenNhaCungCap());
            v.add(ncc.getDiaChiNhaCungCap());
            dtm.addRow(v);
        }
        tb_DanhSachNCC.setModel(dtm);
    }
